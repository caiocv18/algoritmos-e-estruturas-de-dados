# Operações básicas
soma = 5 + 3          # 8
subtracao = 5 - 3     # 2
produto = 5 * 3       # 15
divisao = 5 / 3       # 1.6666666666666667 (sempre retorna um float)

# Operações adicionais
divisao_inteira = 5 // 3   # 1 (retorna apenas a parte inteira)
resto = 5 % 3             # 2 (retorna o resto da divisão)
potencia = 5 ** 3         # 125 (5 elevado à potência 3)

# Operações com atribuição
x = 5
x += 3    # Equivalente a x = x + 3 (x agora é 8)
x -= 2    # Equivalente a x = x - 2 (x agora é 6)
x *= 2    # Equivalente a x = x * 2 (x agora é 12)
x /= 4    # Equivalente a x = x / 4 (x agora é 3.0)