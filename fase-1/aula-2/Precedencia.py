# Operadores têm diferentes níveis de precedência
resultado = 5 + 3 * 2        # 11 (multiplicação tem precedência)

# Parênteses controlam a ordem das operações
resultado = (5 + 3) * 2      # 16 (soma é realizada primeiro)

# Exponenciação tem alta precedência
resultado = 2 + 3 ** 2       # 11 (3² = 9, depois soma 2)

# Operadores de mesma precedência: da esquerda para direita
resultado = 10 / 5 * 2       # 4.0 (divide primeiro, depois multiplica)