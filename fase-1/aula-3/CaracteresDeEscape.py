# Exemplos de caracteres de escape
print("Uma linha\nOutra linha")  # Nova linha
print("Nome:\tJoão")  # Tabulação
print('Ele disse \'Olá!\'')  # Aspas simples dentro de string com aspas simples
print("Ela disse \"Oi!\"")  # Aspas duplas dentro de string com aspas duplas
print("C:\\Users\\Documents")  # Barra invertida

# Saída:
# Uma linha
# Outra linha
# Nome:    João
# Ele disse 'Olá!'
# Ela disse "Oi!"
# C:\Users\Documents