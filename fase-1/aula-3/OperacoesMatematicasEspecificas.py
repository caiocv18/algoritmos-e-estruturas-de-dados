import math

# Funções básicas
print(math.sqrt(16)) # Raiz quadrada: 4.0
print(math.pow(2, 3)) # Potência: 8.0
print(math.fabs(-5)) # Valor absoluto: 5.0
# Funções trigonométricas (em radianos)
print(math.sin(math.pi/2)) # Seno de π/2: 1.0
print(math.cos(0)) # Cosseno de 0: 1.0
print(math.tan(math.pi/4)) # Tangente de π/4: 1.0
# Constantes matemáticas
print(math.pi) # π: 3.141592653589793
print(math.e) # e: 2.718281828459045
print(math.tau) # τ (2π): 6.283185307179586
# Arredondamento
print(round(3.7)) # Arredonda para o inteiro mais próximo: 4
print(math.floor(3.7)) # Arredonda para baixo: 3
print(math.ceil(3.2)) # Arredonda para cima: 4
print(round(2.5)) # Nota: round() arredonda para o par mais próximo: 2