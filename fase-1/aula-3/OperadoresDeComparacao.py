x, y = 10, 5

print(x == y)# Igualdade: False
print(x != y)# Diferença: True
print(x > y)# Maior que: True
print(x < y)# Menor que: False
print(x >= y)# Maior ou igual: True
print(x <= y)# Menor ou igual: False
# Comparações encadeadas
idade = 25
print(18 <= idade < 65)# True (idade está entre 18 e 65)
# Comparação de strings (baseada na ordem lexicográfica)
print("abc" < "abd")# True
print("Python" == "python")# False (case-sensitive)